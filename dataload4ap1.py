        group_var = df.var()
        #total_mean = df.flatten.mean()
        print("组间变异量：%f" % (group_mean.var() * df.shape[0]))
        print("组内变异量：%f" % (group_var.mean()))
        k = df.shape[1]
        n = df.shape[0]
        print("自由度k:%d" % (k-1))
        print("自由度n:%d" % abs(n-k))
        msr=group_mean.var() * df.shape[0]/(k-1)
        mse=group_var.mean()/abs(n-k)
        F = msr/mse
        print("F值:%.3f" % F)
        f_stats=stats.f.cdf(F, k - 1, abs(n-k))
        print("f:",f_stats)
        print("p值:%.30f" % (1 - 2*abs(0.5 - f_stats)))

